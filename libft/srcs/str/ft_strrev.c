#include "libft.h"

char	*ft_strrev(char *s)
{
	int i;
	int j;

	i = 0;
	j = ft_strlen(s) - 1;
	while (i <= j)
	{
		ft_swap(&s[i], &s[j]);
		i++;
		j--;
	}
	return (s);
}
