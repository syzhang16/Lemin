#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int value;
	int sign;

	i = 0;
	sign = 1;
	value = 0;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t' || str[i] == '\v'
			|| str[i] == '\b' || str[i] == '\r' || str[i] == '\f')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		value = (value * 10) + ((int)str[i] - '0');
		i++;
	}
	return (sign * value);
}
